// 3. snake_case (underscore separated)
let my_name="Pratik"; 
let my_age=25; 

// 4.screaming_snake_case (uppercase underscore separated ..... constants)
const MAX_SIZE=100;
const API_KEY="1234567890";
const DATABASE_URL="https://example.com/database";

// 5. Hungarian Notation (prefixing variable names with a type indicator)
let strName="Pratik"; // string
let intAge=25; // integer
let boolIsStudent=true; // boolean
