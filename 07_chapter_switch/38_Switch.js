// switch statment

let day=5;

switch (day){
    case 1:
        console.log("its monday");
        break   
    case 2:
        console.log("its tues");
        break
    case 3:
        console.log("its wed");
        break
    case 4:
        console.log("its thur");
        break
    case 5:
        console.log("its fri");
        break
    case 6:
        console.log("its sat");
        break
    default:
        console.log("its sunday");

}
